#include"Component.h"

//===============================================
//	グローバル変数
//===============================================
vector<Component*> Component::g_Index;

//===============================================
//	関数
//===============================================

//-------------------------------------
//	処理開始
//-------------------------------------
void Component::g_Begin()
{

}

//-------------------------------------
//	初期化処理
//-------------------------------------
void Component::g_Initialize()
{
	for (UINT i = 0; i < g_Index.size(); i++)
	{
		g_Index.at(i)->Initialize();
	}
}

//-------------------------------------
//	前更新
//-------------------------------------
void Component::g_BeginUpdate()
{
	for (UINT i = 0; i < g_Index.size(); i++)
	{
		g_Index.at(i)->BeginUpdate();
	}
}

//-------------------------------------
//	描画
//-------------------------------------
void Component::g_Render()
{
	for (UINT i = 0; i < g_Index.size(); i++)
	{
		g_Index.at(i)->Render();
	}
}

//-------------------------------------
//	後更新
//-------------------------------------
void Component::g_EndUpdate()
{
	for (UINT i = 0; i < g_Index.size(); i++)
	{
		g_Index.at(i)->EndUpdate();
	}
}

//-------------------------------------
//	終了
//-------------------------------------
void Component::g_UnInitialize()
{
	for (UINT i = 0; i < g_Index.size(); i++)
	{
		g_Index.at(i)->UnInitialize();
	}
}

