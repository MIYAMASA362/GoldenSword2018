//-------------------------------------
//	キューブ色
//-------------------------------------
void Set_CubeColor(D3DCOLOR Color)
{
	g_CubeColor = Color;
	for(int i = 0; i < sizeof(Cube)/sizeof(Cube[0]); i++)
	{
		Cube[i].Color = g_CubeColor;
	}
}

//-------------------------------------
//	モデル取得
//-------------------------------------
void* GetModel_Cube()
{
	return &Cube[0];
}